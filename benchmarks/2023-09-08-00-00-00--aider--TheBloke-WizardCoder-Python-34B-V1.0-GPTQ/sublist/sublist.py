# Possible sublist categories.
# Change the values as you see fit.
SUBLIST = 0
SUPERLIST = 1
EQUAL = 2
UNEQUAL = 3

def is_sublist(A, B):
    if A == B:
        return EQUAL
    elif A in B:
        return SUBLIST
    elif B in A:
        return SUPERLIST
    else:
        return UNEQUAL
